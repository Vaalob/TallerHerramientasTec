    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtproducto.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes seleccionar producto");
            btnBuspro.requestFocus();
            return;
        }

        if(txtcantidad.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar cantidad de compra");
            txtcantidad.requestFocus();
            return;
        }

        if(txtprecio_con.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el precio del producto");
            txtprecio_con.requestFocus();
            return;
        }

        Consumo dts = new Consumo();
        Consum func = new Consum();

        dts.setIdcita(Integer.parseInt(txtidcita.getText()));
        dts.setIdproducto(Integer.parseInt(txtidproducto.getText()));
        dts.setCantidad(Integer.parseInt(txtcantidad.getText()));
        dts.setPrecio_consumo(Double.parseDouble(txtprecio_con.getText()));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La compra " + txtproducto.getText() + " del cliente " + txtcliente.getText() 
                        + " se registro exitosamente");
                mostrar(idcita);
                inhabilitar();
            }

        }

        else if(accion.equals("editar")){
            dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El consumo de " + txtcliente.getText() + " ha sido editado exitosamente");
                mostrar(idcita);
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

