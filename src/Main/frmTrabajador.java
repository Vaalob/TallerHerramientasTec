    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del trabajador");
            txtNombre.requestFocus();
            return;
        }

        if(txtApellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el apellido del trabajador");
            txtApellido.requestFocus();
            return;
        }

        if(txtTelefono.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el telefono del trabajador");
            txtTelefono.requestFocus();
            return;
        }
        
        if(txtEmail.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el email del trabajador");
            txtEmail.requestFocus();
            return;
        }
        
        if(txtLogin.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el login del trabajador");
            txtLogin.requestFocus();
            return;
        }
        
        if(txtPass.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la contrase√±a del trabajador");
            txtPass.requestFocus();
            return;
        }
        
        Trabajador dts = new Trabajador();
        Worker func = new Worker();

        
        
        dts.setNombre(txtNombre.getText());
        dts.setApellido(txtApellido.getText());
        dts.setTelefono(txtTelefono.getText());
        dts.setEmail(txtEmail.getText());
        
        int selecc = comAcc.getSelectedIndex();
        dts.setAcceso((String) comAcc.getItemAt(selecc));
        
        dts.setLogin(txtLogin.getText());
        dts.setPassword(txtPass.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El trabajador se registro exitosamente");
                mostrar("");
                inhabilitar();
            }

        }

        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El trabajador fue editado exitosamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

